export const t = (lang: Language, key: keyof typeof translations['en'], params?: Record<string, string>) => {
  // Fallback to English if translation key is missing in other languages
  let text = translations[lang]?.[key] || translations['en'][key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, v);
    });
  }
  return text;
};